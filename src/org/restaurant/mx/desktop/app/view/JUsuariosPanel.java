    private void seleccionarPrimer() {
        if (dlm.size() != 0) {
            lstUsuarios.setSelectedIndex(0);
            lstUsuariosMouseClicked(null);
        }
    }

    private void cargarUsuarios() {
        dlm = new DefaultListModel();
        List<EmpleadoInfo> emps = new ArrayList<EmpleadoInfo>();
        emps = conEmp.listarPorActivo(EmpleadoInfo.EMP_ACTIVO);
        for (EmpleadoInfo e : emps) {
            dlm.addElement(e);
        }
        lstUsuarios.setModel(dlm);
    }

    private void limpiarCampos() {
        JTFApellidos.setText(null);
        JTFCiudad.setText(null);
        JTFCorreo.setText(null);
        JTFDireccion.setText(null);
        JTFNombre.setText(null);
        JTFTelefono.setText(null);
        JTFUsuario.setText(null);
        JCBTipo.setSelectedIndex(0);
        JPContraseña.setText(null);
    }

    public boolean validarCampos() {
        boolean valido = false;
        if (!JTFNombre.getText().isEmpty()
                && !JTFApellidos.getText().isEmpty()
                && !JTFUsuario.getText().isEmpty()
                && !JPContraseña.getText().isEmpty()
                && !JTFCiudad.getText().isEmpty()
                && !JTFCorreo.getText().isEmpty()
                && !JTFTelefono.getText().isEmpty()) {

            valido = true;
        }
        return valido;
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (validarCampos()) {
            EmpleadoInfo emp = new EmpleadoInfo();
            emp.setNoombres(JTFNombre.getText());
            emp.setApellidos(JTFApellidos.getText());
            emp.setUsuario(JTFUsuario.getText());
            emp.setPassword(JPContraseña.getText());
            emp.setActivo(true);
            emp.setCiudad(JTFCiudad.getText());
            emp.setDireccion(JTFDireccion.getText());
            emp.setCorreo(JTFCorreo.getText());
            emp.setTelefono(JTFTelefono.getText());
            emp.setTipoEmpleado(JCBTipo.getSelectedIndex() + 1);
            if (conEmp.agregar(emp)) {
                JOptionPane.showMessageDialog(this, "El empleado se logró guardar con éxito",
                        "Éxito",
                        JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
                cargarUsuarios();
            } else {
                JOptionPane.showMessageDialog(this, "Se ha generado un error interno al guardar el empleado.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);

            }
        } else {
            JOptionPane.showMessageDialog(this, "No es posible guardar porque algunos de los campos se cuentra vacíos.",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

