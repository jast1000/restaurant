    private void lstUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstUsuariosMouseClicked
        btnGuardar.setEnabled(false);
        limpiarCampos();
        EmpleadoInfo emp = (EmpleadoInfo) lstUsuarios.getSelectedValue();
        JTFApellidos.setText(emp.getApellidos());
        JTFCiudad.setText(emp.getCiudad());
        JTFCorreo.setText(emp.getCorreo());
        JTFDireccion.setText(emp.getDireccion());
        JTFNombre.setText(emp.getNoombres());
        JTFTelefono.setText(emp.getTelefono());
        JTFUsuario.setText(emp.getTelefono());
        JPContraseña.setText(new Encriptador(emp.getPassword()).desencriptar());
        Integer tipoUsuario = 0;
        switch(emp.getTipoEmpleado()) {
            case 1:
                tipoUsuario = 1;
                break;
            case 3:
                tipoUsuario = 0;
        }
        JCBTipo.setSelectedIndex(tipoUsuario);
    }//GEN-LAST:event_lstUsuariosMouseClicked

